# Define a string
s = "pythonisfun"

# Slice the string from index 1 to index 4 (exclusive)
print(s[1:4]) 

# Slice the string from index 2 to the end (inclusive)
print(s[2:])  

# Slice the string from the start to index 4 (exclusive)
print(s[:4])  

# Slice the string with a step of 2
print(s[::2])  