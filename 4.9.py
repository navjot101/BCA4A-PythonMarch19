# Define the first term and the last term of the series
first_term = 3
last_term = 30

# Calculate the number of terms in the series
num_terms = (last_term - first_term) // 3 + 1

# Use the arithmetic series formula to calculate the sum
sum_of_series = (first_term + last_term) * num_terms / 2

# Print the sum of the series
print("Sum of the series:", sum_of_series)
